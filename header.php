<script type="text/javascript">
    $(document).ready( function() {
    $('#myCarousel').carousel({
        interval:   111000
    });

    var clickEvent = false;
    $('#myCarousel').on('click', '.nav a', function() {
            clickEvent = true;
            $('.nav li.slider-nav').removeClass('active triangle-up');
            $(this).parent().addClass('active triangle-up');
    }).on('slid.bs.carousel', function(e) {
        if(!clickEvent) {
            var count = $('.nav li.slider-nav').children().length -1;
            var current = $('.nav li.slider-nav.active');
            current.removeClass('active').next().addClass('active triangle-up');
            var id = parseInt(current.data('slide-to'));
            if(count == id) {
                $('.nav li').first().addClass('active triangle-up');
            }
        }
        clickEvent = false;
    });
    });
</script>
